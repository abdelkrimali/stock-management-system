    void getreference()
    {
        try {
            setconnection();
            String select ="select reference,quantite_reste,prix_vente from produit where designation='"+designation_produit+"'";
            stmt=con.createStatement();
            rs=stmt.executeQuery(select);
           while(rs.next()){
                reference= Integer.parseInt(rs.getString(1));
                reststock.setText(rs.getString(2));
                prixunitaire.setText(rs.getString(3));
            }
        } catch (Exception e) {
        }finally{
            try {
                con.close();
                stmt.close();
                rs.close();
            } catch (Exception e) {
            }
        }
        
    }
     
