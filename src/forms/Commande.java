    private void txtqteKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtqteKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
            double rest=Double.parseDouble(reststock.getText());
            double qte=Double.parseDouble(txtqte.getText());
            double prix=Double.parseDouble(prixunitaire.getText());
            if(rest>=qte){
                 double montant =qte*prix;
             txtmontant.setText(String.valueOf(montant));
            }else
            {
                Tools.msgBox("Quantité n'est pas valide !!");
                txtqte.setText(String.valueOf(rest));
            }
           
        }
    }//GEN-LAST:event_txtqteKeyPressed

