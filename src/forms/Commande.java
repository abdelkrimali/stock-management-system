    void count_produit() throws SQLException
    {
       try{
           
            setconnection();
            String select ="select count(*) from produit ";
            stmt=con.createStatement();
	     rs=stmt.executeQuery(select);
             while(rs.next()){
                count_produit= Integer.parseInt(rs.getString(1));
             }
            
        }catch(Exception e){
            e.printStackTrace();
        }finally{
           
            con.close();
            stmt.close();
            rs.close();
        }
    }
